function makeProgram(
  id: string,
  name: string,
  degree: string,
  faculty: string,
  credits: number,
  duration: string,
  tuitionPerCredit: number,
  admissionFee: number,
  labFee: number,
  intakeSeasons: string[] = ['Spring', 'Summer', 'Fall'],
  weekend = false,
  online = false
): Program {
  return {
    id,
    name,
    degree,
    faculty,
    credits,
    duration,
    tuitionPerCredit,
    admissionFee,
    labFee,
    libraryFee: 2000,
    otherFee: 3000,
    scholarshipAvailable: true,
    minCgpa: 3.0,
    intakeSeasons,
    weekend,
    online,
  };
}

function makeReview(
  id: string,
  author: string,
  rating: number,
  text: string,
  verified = true
): Review {
  return {
    id,
    author,
    verified,
    date: '2025-0' + (Math.floor(Math.random() * 8) + 1) + '-15',
    rating,
    categories: {
      teachers: rating,
      campus: Math.min(5, rating + (Math.random() > 0.5 ? 1 : 0)),
      labs: Math.max(3, rating - (Math.random() > 0.5 ? 1 : 0)),
      environment: rating,
      career: Math.max(3, rating - (Math.random() > 0.5 ? 1 : 0)),
      library: Math.min(5, rating + (Math.random() > 0.5 ? 1 : 0)),
    },
    text,
  };
}

