export function getStats() {
  const totalPrograms = universities.reduce((sum, u) => sum + u.programs.length, 0);
  const allTuitions = universities.flatMap((u) => u.programs.map((p) => p.tuitionPerCredit));
  const avgTuition = Math.round(allTuitions.reduce((a, b) => a + b, 0) / allTuitions.length);
  const minTuition = Math.min(...allTuitions);
  const maxTuition = Math.max(...allTuitions);
  const iebCount = universities.filter((u) => u.iebAccredited).length;
  const scholarshipCount = universities.filter((u) => u.hasScholarship).length;

  return {
    totalUniversities: universities.length,
    totalPrograms,
    avgTuition,
    minTuition,
    maxTuition,
    iebCount,
    scholarshipCount,
  };
}
